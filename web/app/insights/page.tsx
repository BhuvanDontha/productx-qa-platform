"use client";
import { useState, useEffect } from "react";
import { motion } from "framer-motion";
import { Sparkles, Printer, Loader2, AlertTriangle, CheckCircle, ArrowRight } from "lucide-react";
import Link from "next/link";
import InsightPanel from "@/components/InsightPanel";
import type { MetricsSummary } from "@/types";

const f1Color = (f1: number) =>
  f1 >= 0.8 ? "text-[#188038]" : f1 >= 0.65 ? "text-[#b06000]" : "text-[#c5221f]";

export default function InsightsPage() {
  const [metrics, setMetrics] = useState<MetricsSummary | null>(null);
  const [insight, setInsight] = useState<string | null>(null);
  const [loading, setLoading] = useState(false);
  const [generatedAt, setGeneratedAt] = useState<string | null>(null);
  const [fallback, setFallback] = useState(false);

  useEffect(() => {
    if (typeof window !== "undefined") {
      const stored = localStorage.getItem("qa_metrics");
      if (stored) {
        try { setMetrics(JSON.parse(stored)); } catch { /* ignore */ }
      }
    }
  }, []);

  // Locally-composed brief — guarantees output even if the API is unreachable.
  function localBrief(m: MetricsSummary): string {
    const flagged = m.by_reviewer.filter(r => r.needs_flag);
    const best = [...m.by_modality].sort((a, b) => b.f1 - a.f1)[0];
    const worst = [...m.by_modality].sort((a, b) => a.f1 - b.f1)[0];
    const acc = (m.overall_accuracy * 100).toFixed(1);

    const flagLine = flagged.length
      ? `${flagged.map(r => r.reviewer_id).join(", ")} ${flagged.length > 1 ? "fall" : "falls"} below the 60% accuracy threshold and ${flagged.length > 1 ? "require" : "requires"} immediate retraining.`
      : `No reviewer currently falls below the 60% retraining threshold.`;

    return [
      `Overall QA health stands at ${acc}% accuracy across ${m.total_reviews} reviewed decisions. ${best ? `The ${best.modality} modality is the strongest performer at an F1 of ${(best.f1 * 100).toFixed(1)}%, indicating reliable reviewer judgement in that channel.` : ""}`,
      `${worst ? `The ${worst.modality} modality is the weakest at an F1 of ${(worst.f1 * 100).toFixed(1)}%, posing the greatest moderation risk and warranting closer supervision.` : ""} ${flagLine}`,
      `Recommendation: route the flagged reviewers' decisions through a second-pass QA agent and prioritise targeted retraining on their weakest modality. Running this multi-agent pipeline continuously could reclaim an estimated 30–40% of manual QA hours while surfacing systematic errors far earlier than periodic manual audits.`,
    ].filter(Boolean).join("\n\n");
  }

  async function generateInsight() {
    if (!metrics) return;
    setLoading(true);
    setInsight(null);
    setFallback(false);
    try {
      const res = await fetch("/api/generate-insights", {
        method: "POST",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify({ metrics }),
      });
      const data = await res.json();
      if (!res.ok || data.error || !data.insight) throw new Error(data.error || "Empty response");
      setInsight(data.insight);
      setGeneratedAt(data.generated_at);
    } catch (err) {
      // Never show a raw error during a live demo — fall back to a locally-composed brief.
      console.error(err);
      setInsight(localBrief(metrics));
      setGeneratedAt(new Date().toISOString());
      setFallback(true);
    } finally {
      setLoading(false);
    }
  }

  return (
    <div className="mx-auto max-w-7xl px-6 py-10 space-y-8">
      <div>
        <h1 className="text-3xl font-extrabold text-[#202124]">Leadership Brief</h1>
        <p className="text-[#5f6368] mt-1">
          AI-synthesized executive summary from QA metrics, generated by Gemini 2.5 Flash.
        </p>
      </div>

      {!metrics && (
        <div className="rounded-xl border border-[#fde293] bg-[#fef7e0] p-6 text-[#b06000] flex flex-col sm:flex-row sm:items-center gap-4">
          <div className="flex items-center gap-3">
            <AlertTriangle className="h-5 w-5 flex-shrink-0" />
            <p>No QA metrics found yet. Run the analysis on the Dashboard first.</p>
          </div>
          <Link
            href="/dashboard"
            className="inline-flex items-center gap-2 rounded-lg bg-[#4285F4] hover:bg-[#1a73e8] px-4 py-2 text-sm font-semibold text-white sm:ml-auto whitespace-nowrap"
          >
            Go to Dashboard <ArrowRight className="h-4 w-4" />
          </Link>
        </div>
      )}

      {metrics && (
        <div className="grid grid-cols-1 md:grid-cols-2 gap-8">
          {/* Left: Metrics Summary */}
          <div className="space-y-6">
            <div className="rounded-xl border border-[#dadce0] bg-white p-6 space-y-4 shadow-sm">
              <h2 className="text-lg font-semibold text-[#202124]">Metrics Summary</h2>

              <div>
                <p className="text-xs text-[#80868b] uppercase tracking-wider mb-3">Modality F1 Scores</p>
                <div className="space-y-2">
                  {metrics.by_modality.map((m) => (
                    <div key={m.modality} className="flex items-center justify-between">
                      <span className="capitalize text-sm text-[#3c4043]">{m.modality}</span>
                      <span className={`font-bold text-sm ${f1Color(m.f1)}`}>
                        {(m.f1 * 100).toFixed(1)}%
                      </span>
                    </div>
                  ))}
                </div>
              </div>

              <div className="border-t border-[#e8eaed] pt-4">
                <p className="text-xs text-[#80868b] uppercase tracking-wider mb-3">Reviewer Status</p>
                <div className="space-y-2">
                  {metrics.by_reviewer.map((r) => (
                    <div key={r.reviewer_id} className="flex items-center justify-between">
                      <div className="flex items-center gap-2">
                        {r.needs_flag ? (
                          <AlertTriangle className="h-3 w-3 text-[#EA4335]" />
                        ) : (
                          <CheckCircle className="h-3 w-3 text-[#34A853]" />
                        )}
                        <span className="text-sm text-[#3c4043]">{r.reviewer_id}</span>
                      </div>
                      <span className={`text-sm font-medium ${r.needs_flag ? "text-[#c5221f]" : "text-[#3c4043]"}`}>
                        {(r.accuracy * 100).toFixed(1)}%
                        {r.needs_flag && <span className="ml-1 text-xs text-[#EA4335]">⚠ Flag</span>}
                      </span>
                    </div>
                  ))}
                </div>
              </div>

              <div className="border-t border-[#e8eaed] pt-4 flex justify-between items-center">
                <span className="text-sm text-[#5f6368]">Overall Accuracy</span>
                <span className="text-xl font-extrabold text-[#4285F4]">
                  {(metrics.overall_accuracy * 100).toFixed(1)}%
                </span>
              </div>
            </div>
          </div>

          {/* Right: Executive Brief */}
          <div className="space-y-4">
            <div className="flex items-center justify-between">
              <h2 className="text-lg font-semibold text-[#202124]">Executive Brief</h2>
              {insight && (
                <button
                  onClick={() => window.print()}
                  className="flex items-center gap-1.5 rounded-lg border border-[#dadce0] bg-white px-3 py-1.5 text-xs text-[#5f6368] hover:text-[#202124] transition-colors"
                >
                  <Printer className="h-3 w-3" /> Print / Export
                </button>
              )}
            </div>

            <button
              onClick={generateInsight}
              disabled={loading || !metrics}
              className="w-full flex items-center justify-center gap-2 rounded-xl bg-[#4285F4] hover:bg-[#1a73e8] disabled:opacity-50 disabled:cursor-not-allowed px-6 py-3 font-semibold text-white transition-all shadow-md"
            >
              {loading ? (
                <><Loader2 className="h-4 w-4 animate-spin" /> Insight Agent synthesizing...</>
              ) : (
                <><Sparkles className="h-4 w-4" /> Generate Executive Brief</>
              )}
            </button>

            {loading && (
              <motion.div
                initial={{ opacity: 0 }}
                animate={{ opacity: [0.4, 1, 0.4] }}
                transition={{ duration: 1.5, repeat: Infinity }}
                className="rounded-xl border border-[#aecbfa] bg-white p-6 text-center"
              >
                <p className="text-[#1a73e8] text-sm">Gemini 2.5 Flash is analyzing metrics and composing the executive brief...</p>
              </motion.div>
            )}

            {insight && !loading && (
              <>
                <InsightPanel insight={insight} />
                <p className="text-xs text-[#80868b] text-center">
                  {generatedAt && <>Generated {new Date(generatedAt).toLocaleString()} · </>}
                  {fallback ? "Composed locally from metrics (live model unavailable)" : "Google Gemini 2.5 Flash"}
                </p>
              </>
            )}
          </div>
        </div>
      )}
    </div>
  );
}
